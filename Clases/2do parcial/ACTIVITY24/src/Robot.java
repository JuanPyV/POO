
public class Robot {

	private String model, name;
	private int year;
	
	public String getModel() {
		return model;
	}
	public String getName() {
		return name;
	}
	public int getYear() {
		return year;
	}
	
	
	public Robot(String model, String name, int year) {
		super();
		this.model = model;
		this.name = name;
		this.year = year;
	}
	
	

}
