
public class Robot {
	
	public String model;
	public int year;
	public String name;
	
	
	public String getModel() {
		return model;
	}
	public int getYear() {
		return year;
	}
	public String getName() {
		return name;
	}
	
	public Robot(String model, int year, String name) {
		super();
		this.model = model;
		this.year = year;
		this.name = name;
	}
	
}
